const _byId = new Map(ITEMS.map((i) => [i.id, i] as const));

/**
 * Placeholder item def returned by `itemById` when a lookup fails. Lets the UI
 * render a recognizable "unknown" tile instead of crashing the whole React
 * tree. We log a console.warn so the bug is still visible in dev.
 */
const UNKNOWN_ITEM: ItemDef = {
  id: "__unknown",
  name: "Unknown",
  natureType: "Universal",
  size: 1,
  minRarity: "Wooden",
  maxRarity: "Wooden",
  baseCooldown: 999,
  keywords: [],
  baseEffects: {},
  description: "Missing item definition.",
};

export function itemById(id: string): ItemDef {
  const it = _byId.get(id);
  if (!it) {
    // eslint-disable-next-line no-console
    console.warn(`[items] Unknown item def: "${id}" — falling back to placeholder.`);
    return UNKNOWN_ITEM;
  }
  return it;
}

/** Strict variant — throws. Use only in code paths where missing data is a hard bug. */
export function strictItemById(id: string): ItemDef {
  const it = _byId.get(id);
  if (!it) throw new Error(`Unknown item def: ${id}`);
  return it;
}

/** Items legal to appear in the shop pool (excludes Diamond-only entries). */
export const SHOP_POOL_ITEMS: ItemDef[] = ITEMS.filter(
  (i) => i.minRarity !== "Diamond"
);
